package Module_4_BASICS;

public class arithmeticOperationsOnInt {
    public static void main(String[] args) {
        int x = 5;
        int y = 2;
        System.out.println(x+y);
        System.out.println(x-y);
        System.out.println(x*y);
        System.out.println(x/y);

    }
}


// Output:
//7
//3
//10
//2