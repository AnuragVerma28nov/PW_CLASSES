package Module_4_BASICS;

public class relationalOperators {
    public static void main(String[] args) {
        // create variables
        int p = 10, q = 15;
        // == operator
        System.out.println(p == q); // false
        // != operator
        System.out.println(p != q); // true
        // > operator
        System.out.println(p > q); // false
        // < operator
        System.out.println(p < q); // true
        // >= operator
        System.out.println(p >= q); // false
        // <= operator
        System.out.println(p <= q); // true
    }
}

//Output:
//false
//true
//false
//true
//false
//true