package Module_4_BASICS;

public class incementDecrement {
    public static void main(String[] args) {
        int x = 5;
        x ++;
        System.out.println(x);
        x --;
        System.out.println(x);
        ++ x;
        System.out.println(x);
        -- x;
        System.out.println(x);

    }
}

