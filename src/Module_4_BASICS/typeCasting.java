package Module_4_BASICS;

public class typeCasting {
    public static void main(String[] args) {
        int a = 6;
        System.out.println((double)a);
        double b = 6.23;
        System.out.println((int) b);
        char c = 'A';
        System.out.println((int) c);

    }
}

//Output:
//6.0
//6
//65