package Module_4_BASICS;

public class updatingVariables {
    public static void main(String[] args) {
        int x = 5;
        System.out.println(x);

        x = 7;
        System.out.println(x);

        x += 6; //x = x + 6;
        System.out.println(x);

        x -= 20; //x = x - 20;
        System.out.println(x);

    }
}

//Output
//5
//7
//13
//-7